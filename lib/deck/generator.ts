function renderCompetitorSlide(pptx: PptxInstance, data: Slide) {
  const slide = pptx.addSlide();
  slide.background = { color: C.offwhite };
  addHeaderBand(slide, data.title, data.subtitle, data.slide_number, C.blue);
  addFooter(slide);

  const c = data.content as CompetitorContent;

  // Position statement
  roundRect(slide, 0.3, 1.52, 9.4, 0.5, C.blueLight, C.blue, 0.08);
  slide.addText(`💡  ${c.our_position}`, {
    x: 0.45, y: 1.58, w: 9.1, h: 0.38,
    fontSize: 12, bold: true, color: C.blue, fontFace: "Arial", wrap: true,
  });

  // Table
  const tableY = 2.15;
  const colW = [2.3, 2.5, 2.5, 1.4, 0.7];
  const headers = ["Competitor", "Strength", "Weakness", "Threat", "Type"];
  let cx = 0.3;

  // Header row
  slide.addShape("rect" as Parameters<typeof slide.addShape>[0], {
    x: 0.3, y: tableY, w: 9.4, h: 0.42,
    fill: { color: C.ink },
    line: { width: 0 },
  });
  headers.forEach((h, i) => {
    slide.addText(h, {
      x: cx + 0.08, y: tableY + 0.08, w: colW[i] - 0.1, h: 0.28,
      fontSize: 9, bold: true, color: C.white, fontFace: "Arial",
    });
    cx += colW[i];
  });

  // Data rows
  const threatColors: Record<string, string> = { high: C.red, medium: C.amber, low: C.green };
  c.competitors.forEach((comp, rowIdx) => {
    const rowY = tableY + 0.42 + rowIdx * 0.48;
    const rowBg = rowIdx % 2 === 0 ? C.white : C.card;
    slide.addShape("rect" as Parameters<typeof slide.addShape>[0], {
      x: 0.3, y: rowY, w: 9.4, h: 0.46,
      fill: { color: rowBg },
      line: { color: C.slate2, width: 1 },
    });

    let rx = 0.3;
    const cells = [comp.name, comp.their_strength, comp.their_weakness, comp.threat_level.toUpperCase(), comp.threat_level === "high" ? "⚠" : comp.threat_level === "medium" ? "●" : "✓"];
    const cellFg = [C.ink, C.slate6, C.slate6, C.white, C.white];
    const cellBg = ["", "", "", threatColors[comp.threat_level] ?? C.slate5, threatColors[comp.threat_level] ?? C.slate5];
    cells.forEach((cell, ci) => {
      if (cellBg[ci]) {
        slide.addShape("rect" as Parameters<typeof slide.addShape>[0], {
          x: rx, y: rowY, w: colW[ci], h: 0.46,
          fill: { color: cellBg[ci] },
          line: { width: 0 },
        });
      }
      slide.addText(cell, {
        x: rx + 0.08, y: rowY + 0.1, w: colW[ci] - 0.1, h: 0.28,
        fontSize: 10, color: cellFg[ci], bold: ci === 0 || ci === 3, fontFace: "Arial",
        align: ci >= 3 ? "center" : "left",
      });
      rx += colW[ci];
    });
  });

  // Advantage banner
  const bannerY = tableY + 0.42 + c.competitors.length * 0.48 + 0.12;
  roundRect(slide, 0.3, bannerY, 9.4, 0.48, C.saffron, undefined, 0.08);
  slide.addText(`🏆  Our Advantage:  ${c.our_advantage}`, {
    x: 0.45, y: bannerY + 0.08, w: 9.1, h: 0.32,
    fontSize: 12, bold: true, color: C.white, fontFace: "Arial",
  });
}

