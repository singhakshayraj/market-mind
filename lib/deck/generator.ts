// ── HELPERS ────────────────────────────────────────────────────────────────────

function addHeaderBand(slide: PptxSlide, title: string, subtitle: string | null, slideNum: number, accentColor = C.saffron) {
  // Dark header band
  slide.addShape("rect" as Parameters<typeof slide.addShape>[0], {
    x: 0, y: 0, w: 10, h: 1.35,
    fill: { color: C.ink },
    line: { width: 0 },
  });
  // Accent stripe bottom of header
  slide.addShape("rect" as Parameters<typeof slide.addShape>[0], {
    x: 0, y: 1.3, w: 10, h: 0.07,
    fill: { color: accentColor },
    line: { width: 0 },
  });
  // Slide number pill top-right
  slide.addShape("roundRect" as Parameters<typeof slide.addShape>[0], {
    x: 9.0, y: 0.15, w: 0.7, h: 0.38,
    fill: { color: accentColor },
    line: { width: 0 },
    rectRadius: 0.08,
  });
  slide.addText(String(slideNum).padStart(2, "0"), {
    x: 9.0, y: 0.18, w: 0.7, h: 0.32,
    fontSize: 12, bold: true, color: C.white, align: "center", fontFace: "Arial",
  });
  // Title
  slide.addText(title, {
    x: 0.35, y: 0.12, w: 8.5, h: 0.58,
    fontSize: 26, bold: true, color: C.white, fontFace: "Arial",
  });
  // Subtitle
  if (subtitle) {
    slide.addText(subtitle, {
      x: 0.35, y: 0.72, w: 8.5, h: 0.32,
      fontSize: 12, color: accentColor, fontFace: "Arial", italic: true,
    });
  }
}

function addFooter(slide: PptxSlide) {
  slide.addShape("rect" as Parameters<typeof slide.addShape>[0], {
    x: 0, y: 5.45, w: 10, h: 0.18,
    fill: { color: C.card },
    line: { width: 0 },
  });
  slide.addText("Generated by MarketMind AI  ·  marketmind.ai", {
    x: 0.3, y: 5.45, w: 9.4, h: 0.18,
    fontSize: 7, color: C.slate5, align: "center", fontFace: "Arial",
  });
}

function roundRect(
  slide: PptxSlide,
  x: number, y: number, w: number, h: number,
  fillColor: string, borderColor?: string, radius = 0.1
) {
  slide.addShape("roundRect" as Parameters<typeof slide.addShape>[0], {
    x, y, w, h,
    fill: { color: fillColor },
    line: borderColor ? { color: borderColor, width: 1 } : { width: 0 },
    rectRadius: radius,
  });
}

function badge(slide: PptxSlide, text: string, x: number, y: number, w: number, bg: string, fg: string) {
  roundRect(slide, x, y, w, 0.3, bg, undefined, 0.06);
  slide.addText(text, {
    x, y: y + 0.03, w, h: 0.24,
    fontSize: 9, bold: true, color: fg, align: "center", fontFace: "Arial",
  });
}

